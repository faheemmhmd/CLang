// use of modular operator 
#include <stdio.h>

int main(){
   int digit;
   printf("Enter the digit : ");
   scanf("%d",&digit);
   if (digit%2==0)
   {
      printf(" The digit is an Even no. \n");
   }
   else
   {
      printf("The digit is an odd no. \n");
   }
   printf("Thankyou \n");

   return 0;
}